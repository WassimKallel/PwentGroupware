     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store($id,Request $request)
    {
        $comment = new Comment($request->all());
        $post = Post::find($id);
        $comment->post()->associate($post);
        $comment->user()->associate(Auth::user());
        $comment->save();
        return back();
    }

